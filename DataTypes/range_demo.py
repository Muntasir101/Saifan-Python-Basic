# There are 3 ways to create a range object

# range(stop): generate numbers from 0 to stop - 1
r = range(5)  # r represents the sequence 0,1,2,3,4

# range(start,stop): generate numbers from start to stop - 1
r1 = range(2, 5)  # r1 represents the sequence 2,3,4

# range(start, stop, step): generate numbers from start to stop - 1,
# incrementing by step.The step can be positive or negative
r3 = range(1, 10, 2)  # r3 represents the sequence 1,3,5,7,9

