# example: Bucket list
total_money_before_purchase = 2000
phone_price = 1000
shoe_price = 200
clothing_price = 2000
food_price = 50.5
transport_cost = 100

# addition
total_expenses = phone_price + shoe_price + clothing_price + food_price + transport_cost
print("Total Expenses: ", total_expenses)

# subtraction
remaining_money = total_money_before_purchase - total_expenses
print("Remaining money: ", remaining_money)
